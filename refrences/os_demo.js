const os = require("os");
console.log(os.platform());

// CPU Architecture
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Free Memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// home directory
console.log(os.homedir());

// uptime
console.log(os.uptime());
